from pyscf import gto, scf
import numpy as np
from pyblock2._pyscf.ao2mo import integrals as itg
from pyblock2.driver.core import DMRGDriver, SymmetryTypes, MPOAlgorithmTypes

bond_dims = [250] * 4 + [500] * 4
noises = [1e-4] * 4 + [1e-5] * 4 + [0]
thrds = [1e-10] * 8

mol = gto.M(atom="N 0 0 0; N 0 0 1.1", basis="sto3g", symmetry="c1", verbose=0)
mf = scf.GHF(mol).run(conv_tol=1e-14)
ncas, n_elec, spin, ecore, h1e, g2e, orb_sym = itg.get_ghf_integrals(
    mf, ncore=0, ncas=None, g2e_symm=1
)

driver = DMRGDriver(scratch="./tmp", symm_type=SymmetryTypes.SGF, n_threads=4)
driver.initialize_system(n_sites=ncas, n_elec=n_elec, orb_sym=orb_sym)

mpo = driver.get_qc_mpo(h1e=h1e, g2e=g2e, ecore=ecore, iprint=1)
mps = driver.get_random_mps(tag="KET", bond_dim=250, nroots=1)
energy = driver.dmrg(
    mpo,
    mps,
    tol=1e-8,
    n_sweeps=20,
    bond_dims=bond_dims,
    noises=noises,
    thrds=thrds,
    iprint=2,
)

rdm1 = driver.get_1pdm(mps)
ordm1 = driver.get_orbital_entropies(mps, orb_type=1)
print("ordm1:\n", ordm1)

