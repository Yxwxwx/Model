# 3. ordm2 (Two-orbital entropies)
ordm2 = np.zeros((ncas, ncas))
for i in range(ncas):
    for j in range(ncas):
        if i == j:
            ordm2[i, j] = ordm1[i]
        else:
            ni, nj, nij = rdm1[i, i], rdm1[j, j], nn[i, j]
            rho = np.zeros((4, 4))
            rho[0, 0] = 1.0 - ni - nj + nij  # |00>
            rho[1, 1] = nj - nij  # |01>
            rho[2, 2] = ni - nij  # |10>
            rho[3, 3] = nij  # |11>
            # <c*_i c_j> and <c*_j c_i>
            rho[1, 2] = rdm1[j, i]
            rho[2, 1] = rdm1[i, j]

            evals = np.linalg.eigvalsh(rho)
            s2 = -np.sum([v * np.log(v) for v in evals if v > 1e-14])
            ordm2[i, j] = s2

