from pyscf import gto, scf
import numpy as np
from pyblock2._pyscf.ao2mo import integrals as itg
from pyblock2.driver.core import DMRGDriver, SymmetryTypes, MPOAlgorithmTypes

bond_dims = [250] * 4 + [500] * 4
noises = [1e-4] * 4 + [1e-5] * 4 + [0]
thrds = [1e-10] * 8

mol = gto.M(atom="N 0 0 0; N 0 0 1.1", basis="sto3g", symmetry="c1", verbose=0)
mf = scf.GHF(mol).run(conv_tol=1e-14)
ncas, n_elec, spin, ecore, h1e, g2e, orb_sym = itg.get_ghf_integrals(
    mf, ncore=0, ncas=None, g2e_symm=1
)


driver = DMRGDriver(scratch="./tmp", symm_type=SymmetryTypes.SAny, n_threads=4)
# quantum number wrapper (U1 / n_elec)
driver.set_symmetry_groups("U1Fermi")
Q = driver.bw.SX

# [Part A] Set states and matrix representation of operators in local Hilbert space
site_basis, site_ops = [], []
for k in range(ncas):
    basis = [
        (Q(0), 1),
        (Q(1), 1),
    ]
    ops = {
        "": np.eye(2),  # identity
        "C": np.array([[0, 0], [1, 0]]),  # e+
        "D": np.array([[0, 1], [0, 0]]),  # e
    }
    site_ops.append(ops)
    site_basis.append(basis)

# [Part B] Set Hamiltonian terms in NEO model
driver.initialize_system(n_sites=ncas, vacuum=Q(0), target=Q(n_elec), hamil_init=False)
driver.ghamil = driver.get_custom_hamiltonian(site_basis, site_ops)
b = driver.expr_builder()
# electron-electron interaction
for i in range(ncas):
    for j in range(ncas):
        if abs(h1e[i, j]) > 1e-12:
            b.add_term("CD", [i, j], h1e[i, j])

for i in range(ncas):
    for j in range(ncas):
        for k in range(ncas):
            for l in range(ncas):
                if abs(g2e[i, j, k, l]) > 1e-12:
                    b.add_term("CCDD", [i, k, l, j], 0.5 * g2e[i, j, k, l])

b.add_const(ecore)

# [Part C] Perform DMRG
mpo = driver.get_mpo(
    b.finalize(adjust_order=True),
    algo_type=MPOAlgorithmTypes.FastBipartite,
    iprint=1,
)
mps = driver.get_random_mps(tag="KET", bond_dim=250, nroots=1)
energy = driver.dmrg(
    mpo,
    mps,
    tol=1e-8,
    n_sweeps=20,
    bond_dims=bond_dims,
    noises=noises,
    thrds=thrds,
    iprint=2,
)
print("DMRG energy = %20.15f" % energy)  # -107.654122447519981


# [Part C] Mutual Information
def measure_obs(term, sites):
    b_obs = driver.expr_builder()
    b_obs.add_term(term, sites, 1.0)
    mpo_obs = driver.get_mpo(b_obs.finalize(), iprint=0)
    return driver.expectation(mps, mpo_obs, mps)


# 1. 1/2-RDM
rdm1 = driver.get_1pdm(mps, npdm_expr="CD")[0]
rdm2 = driver.get_2pdm(mps, npdm_expr="CDCD")[0]

nn = np.zeros((ncas, ncas))
for i in range(ncas):
    for j in range(i, ncas):
        #  <n_i n_j>
        nn[i, j] = nn[j, i] = rdm2[i, i, j, j]

# 2. ordm1 (Single-orbital entropies)
ordm1 = np.zeros(ncas)
for i in range(ncas):
    occ = np.clip(rdm1[i, i], 1e-14, 1.0 - 1e-14)
    ordm1[i] = -occ * np.log(occ) - (1 - occ) * np.log(1 - occ)

